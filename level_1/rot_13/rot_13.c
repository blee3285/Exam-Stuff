#include <unistd.h>

char	rot_13(char c)
{
	int		i;

	if (c >= 'a' && c <= 'z')
	{
		i = c - 97;
		i = (i + 13) % 26;
		i += 97;
	}
	else if (c >= 'A' && c <= 'Z')
	{
		i = c - 65;
		i = (i + 13) % 26;
		i += 65;
	}
	else
		return (c);
	return (i);
}

int		main(int ac, char **av)
{
	int		i;
	char	c;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			c = rot_13(av[1][i]);
			write(1, &c, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
